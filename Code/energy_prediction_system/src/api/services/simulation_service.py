class SimulationService:
    """Serviço de simulação com 16 templates abrangentes e lógica de overrides restrita"""

    @staticmethod
    def get_active_model(db: Session, frequency: str) -> Model | None:
        """Obtém o modelo ativo para uma frequência (normalizada)"""
        normalized_freq = frequency.strip().lower()
        model = db.query(Model).filter(
            Model.model_pred_type == normalized_freq,
            Model.is_active).first()
        return model

    @staticmethod
    def get_template(frequency: str, template_name: str,
                     dataset_type: str = "full") -> dict[str, Any]:
        """
        Obtém o template para uma condição climática, filtrado pelo tipo de dataset.
        """
        template_key = f"{frequency}_{template_name}"
        if template_key not in BASE_TEMPLATES:
            raise ValueError(
                f"Template '{template_key}' não encontrado. "
                f"Opções: {sorted(BASE_TEMPLATES.keys())}")

        full_features = BASE_TEMPLATES[template_key].copy()

        if dataset_type == "full" or dataset_type == "pca":
            return full_features

        if dataset_type == "selected":
            cols = SELECTED_COLS_DAILY if frequency == "daily" else SELECTED_COLS_HOURLY
            return {k: v for k, v in full_features.items() if k in cols}

        return full_features

    @staticmethod
    def validate_overrides(overrides: dict[str, float]) -> list[str]:
        """
        Valida os overrides contra limites físicos e restrições de permissão.
        """
        errors = []
        for feature, value in overrides.items():
            # UC13 Restriction
            if feature not in ALLOWED_OVERRIDES:
                errors.append(
                    f"Override não permitido para a variável '{feature}'. Apenas {ALLOWED_OVERRIDES} são permitidos."
                )
                continue

            # Physical Limits Validation
            if feature in PHYSICAL_LIMITS:
                min_val, max_val = PHYSICAL_LIMITS[feature]
                if value < min_val or value > max_val:
                    errors.append(
                        f"'{feature}' deve estar entre {min_val} e {max_val}, recebeu {value}")
        return errors

    @staticmethod
    def apply_overrides(
            base_features: dict[str, float], overrides: dict[str, float]) -> dict[str, float]:
        """Aplica overrides ao template base."""
        features = base_features.copy()
        if overrides:
            features.update(overrides)
        return features

    @staticmethod
    def run_simulation(
        db: Session,
        frequency: str,
        template_name: str,
        year: int,
        month: int,
        day_of_week: int,
        hour: int | None = None,
        overrides: dict[str, float] | None = None,
    ) -> dict[str, Any]:
        active_model = SimulationService.get_active_model(db, frequency)
        if not active_model:
            raise ValueError(
                f"Nenhum modelo ativo encontrado para {frequency}. Ative um modelo primeiro.")

        frequency = active_model.model_pred_type
        dataset_type = active_model.dataset_selected

        # 1. Obter template (sempre Full se for PCA)
        template_dataset = "full" if dataset_type == "pca" else dataset_type
        features = SimulationService.get_template(
            frequency, template_name, template_dataset)

        # 2. Validar e aplicar overrides (clima)
        if overrides:
            errors = SimulationService.validate_overrides(overrides)
            if errors:
                raise ValueError(f"Erro de validação: {'; '.join(errors)}")
            features = SimulationService.apply_overrides(features, overrides)

        # 3. Sincronizar temporal metadata (substitui valores do template)
        features["year"] = year
        features["month"] = month
        features["day_of_week"] = day_of_week
        if hour is not None:
            features["hour"] = hour

        # Season derived from month (UC13)
        features["season"] = (month % 12 // 3) + 1

        # 4. Predict via InferenceEngine
        engine = get_inference_engine()
        predicted_mw = engine.predict(frequency, features)

        # 5. Top 2 drivers
        top_drivers = (
            [d.strip() for d in active_model.top2_drivers.split(",")]
            if active_model.top2_drivers
            else ["t2m", "day_of_week"]
        )

        logger.info(
            f"Simulação: {template_name}/{frequency}/{dataset_type} -> {predicted_mw:.1f} MW")

        return {
            "predicted_mw": predicted_mw,
            "top_drivers": top_drivers,
        }
